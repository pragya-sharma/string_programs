'''
Aim: What are common method of string
'''
'''
Capitalize Method :
'''
s = input("enter string")
d = s.capitalize()
print(d)

'''
Lower Method:
'''
s = input("enter a string")
s = s.lower()
print(s)

'''
Upper Method:
'''
s = input("enter a string")
a = s.upper()
print(a)
'''
Title Method:
'''
s = input("enter a string")
a = s.title()
print(a)
